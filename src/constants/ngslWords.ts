/**
 * 単語がNGSLに含まれるか確認し、バンドを返す
 * @returns バンド番号（1-3）、含まれない場合は null
 */
export function getNgslBand(word: string): NgslBand | null {
  const band = NGSL_MAP.get(word.toLowerCase());
  return band !== undefined ? band : null;
}

/**
 * バンド番号を表示ラベルに変換
 */
